// primitive data type
// So when a single value is assigned to a variable is called as primitive data type.

// string
let username = "Prakash";
console.log(typeof username);

// number
let input = 99.5;
console.log(typeof input);

// boolean
// If product is added to cart then isProductInCart = true otherwise isProductInCart = false
let isProductInCart = false;
console.log(typeof isProductInCart);

// undefined
let userInput;
console.log(userInput);
console.log(typeof userInput);

// null - no value
let user = null;
console.log(user);
console.log(typeof user);